    private void btnEx8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEx8ActionPerformed
        LP0408_Controle contEx = new LP0408_Controle();
        LP0408_BEAN exBEAN = new LP0408_BEAN();
        
        exBEAN.setNumero(contEx.lerNumero());
        exBEAN.setPrimo(contEx.lerNumPrimo(exBEAN));
        contEx.adicionarDados(exBEAN);
        
        LP0408_View frmEx8 = new LP0408_View();
        frmEx8.txtNum.setText(String.valueOf(exBEAN.getNumero()));
        if(exBEAN.isPrimo() == true){
            frmEx8.txtPrimo.setText("Sim");
        }else{
            frmEx8.txtPrimo.setText("Não");
        }
        this.dispose();
        frmEx8.setVisible(true);
    }//GEN-LAST:event_btnEx8ActionPerformed

