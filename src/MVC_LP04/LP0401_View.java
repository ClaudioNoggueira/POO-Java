    public LP0401_View() {
        initComponents();
        LP0401_Controle contEx = new LP0401_Controle();
        List<LP0401_BEAN> listaNumeros = new ArrayList<LP0401_BEAN>();

        listaNumeros = contEx.listarNumeros();

        DefaultTableModel tbm = (DefaultTableModel) tblDados.getModel();

        for (int i = tbm.getRowCount() - 1; i >= 0; i--) {
            tbm.removeRow(i);
        }
        int i = 0;

        for (LP0401_BEAN exBEANs : listaNumeros) {
            tbm.addRow(new String[1]);
            tblDados.setValueAt(exBEANs.getEntrada(), i, 0);
            tblDados.setValueAt(exBEANs.getDobro(), i, 1);
            i++;
        }
    }

