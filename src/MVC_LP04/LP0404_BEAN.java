/*
4ª) Criar um programa que receba um grau e o converta para radianos através de um
método.
Fórmula: radiano = grau * pi / 180

 */
package MVC_LP04;

public class LP0404_BEAN {
    private float grau, radiano;

    public float getGrau() {
        return grau;
    }

    public void setGrau(float grau) {
        this.grau = grau;
    }

    public float getRadiano() {
        return radiano;
    }

    public void setRadiano(float radiano) {
        this.radiano = radiano;
    }
    
}
