/*
5ª) Criar um programa que mostre qual o maior valor entre dois números, utilizando um
método do tipo void (que não retorna valores). 
 */
package MVC_LP04;

public class LP0405_BEAN {
    private double valor1, valor2, maior;

    public double getMaior() {
        return maior;
    }

    public void setMaior(double maior) {
        this.maior = maior;
    }

    public double getValor1() {
        return valor1;
    }

    public void setValor1(double valor1) {
        this.valor1 = valor1;
    }

    public double getValor2() {
        return valor2;
    }

    public void setValor2(double valor2) {
        this.valor2 = valor2;
    }
    
    
}
